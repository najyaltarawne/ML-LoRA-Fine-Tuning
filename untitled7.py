class SpamDataset(torch.utils.data.Dataset):
    def __init__(self, encodings, labels):
        self.encodings = encodings
        self.labels = labels.reset_index(drop=True)

    def __getitem__(self, idx):
        item = {key: torch.tensor(val[idx]) for key, val in self.encodings.items()}
        item['labels'] = torch.tensor(self.labels[idx])
        return item

    def __len__(self):
        return len(self.labels)

train_dataset = SpamDataset(train_encodings, y_train)
test_dataset  = SpamDataset(test_encodings,  y_test)
print("Datasets ready.")

#This uses Hugging Face Transformers
"""
FOR each epoch:
    FOR each batch:
        1. Predict
        2. Compare with true label
        3. Calculate loss
        4. Update weights
"""
def train_model(model, name):
    training_args = TrainingArguments(
        output_dir=f'./{name}',
        num_train_epochs=5,
        per_device_train_batch_size=16,
        per_device_eval_batch_size=16,
        save_strategy="no",
        report_to="none"
    )
    trainer = Trainer(
        model=model,
        args=training_args,
        train_dataset=train_dataset,
        eval_dataset=test_dataset,
    )
    trainer.train()
    preds = trainer.predict(test_dataset)
    y_pred = np.argmax(preds.predictions, axis=1)
    y_prob = torch.softmax(torch.tensor(preds.predictions), dim=1)[:, 1].numpy()
    return y_pred, y_prob

