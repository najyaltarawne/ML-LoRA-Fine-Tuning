"""
LoRA trains DistilBERT by:
-Freezing the original model
-Adding small trainable matrices (A and B)
-Learning only those
-Slightly modifying outputs
"""

#LoRA fine tunes DistilBERT by adding small trainable updates to selected attention layers (q_lin and v_lin) while keeping the original model weights frozen.
#Q (query) → decides what to look at
#V (value) → decides what information to pass
#R  controls how complex the adjustment is

