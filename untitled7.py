"""# ***Running Across 5 seeds for all models to prove our results***"""

from transformers import set_seed as hf_set_seed

seeds = [42, 7, 123, 2024, 99]

all_scores = {
    "MLP Base": {"f1": [], "acc": []},
    "MLP Enhanced": {"f1": [], "acc": []},
    "DistilBERT": {"f1": [], "acc": []},
    "LoRA Small": {"f1": [], "acc": []},
    "LoRA Large": {"f1": [], "acc": []}
}

for seed in seeds:
    print(f"\n===== Seed {seed} =====")

    # Set all random seeds
    np.random.seed(seed)
    torch.manual_seed(seed)
    hf_set_seed(seed)

    # Split
    X_tr, X_te, y_tr, y_te = train_test_split(
        df['clean_text'], df['label'],
        test_size=0.6, stratify=df['label'], random_state=seed
    )

    # Noise
    y_tr = y_tr.copy()
    y_tr[df.loc[X_tr.index, 'noisy']] = 1 - y_tr[df.loc[X_tr.index, 'noisy']]

    # TF-IDF
    tfidf = TfidfVectorizer(max_features=300)
    X_tr_tfidf = tfidf.fit_transform(X_tr)
    X_te_tfidf  = tfidf.transform(X_te)

    # MLP Base
    base = MLPClassifier(hidden_layer_sizes=(64,), max_iter=1000, random_state=seed)
    base.fit(X_tr_tfidf, y_tr)
    all_scores["MLP Base"]["f1"].append(f1_score(y_te, base.predict(X_te_tfidf)))
    all_scores["MLP Base"]["acc"].append(accuracy_score(y_te, base.predict(X_te_tfidf)))

    # MLP Enhanced
    enh = MLPClassifier(hidden_layer_sizes=(128,64), max_iter=1000, alpha=0.0001,
                        early_stopping=True, random_state=seed)
    enh.fit(X_tr_tfidf, y_tr)
    all_scores["MLP Enhanced"]["f1"].append(f1_score(y_te, enh.predict(X_te_tfidf)))
    all_scores["MLP Enhanced"]["acc"].append(accuracy_score(y_te, enh.predict(X_te_tfidf)))

    # Rebuild datasets for LLMs
    tokenizer = DistilBertTokenizer.from_pretrained('distilbert-base-uncased')
    train_encodings = tokenizer(list(X_tr), truncation=True, padding=True, max_length=128)
    test_encodings  = tokenizer(list(X_te),  truncation=True, padding=True, max_length=128)
    train_dataset = SpamDataset(train_encodings, y_tr)
    test_dataset  = SpamDataset(test_encodings,  y_te)

    # DistilBERT
    base_model = DistilBertForSequenceClassification.from_pretrained('distilbert-base-uncased', num_labels=2)
    y_pred_llm, _ = train_model(base_model, f"base_llm_seed{seed}")
    all_scores["DistilBERT"]["f1"].append(f1_score(y_te, y_pred_llm))
    all_scores["DistilBERT"]["acc"].append(accuracy_score(y_te, y_pred_llm))

    # LoRA Small
    fresh_small = DistilBertForSequenceClassification.from_pretrained('distilbert-base-uncased', num_labels=2)
    model_lora_small = get_peft_model(fresh_small, LoraConfig(
        task_type=TaskType.SEQ_CLS, r=4, lora_alpha=16,
        lora_dropout=0.1, target_modules=["q_lin","v_lin"]))
    y_pred_ls, _ = train_model(model_lora_small, f"lora_small_seed{seed}")
    all_scores["LoRA Small"]["f1"].append(f1_score(y_te, y_pred_ls))
    all_scores["LoRA Small"]["acc"].append(accuracy_score(y_te, y_pred_ls))

    # LoRA Large
    fresh_large = DistilBertForSequenceClassification.from_pretrained('distilbert-base-uncased', num_labels=2)
    model_lora_large = get_peft_model(fresh_large, LoraConfig(
        task_type=TaskType.SEQ_CLS, r=16, lora_alpha=32,
        lora_dropout=0.1, target_modules=["q_lin","v_lin"]))
    y_pred_ll, _ = train_model(model_lora_large, f"lora_large_seed{seed}")
    all_scores["LoRA Large"]["f1"].append(f1_score(y_te, y_pred_ll))
    all_scores["LoRA Large"]["acc"].append(accuracy_score(y_te, y_pred_ll))

    print(f"Seed {seed} complete.")

